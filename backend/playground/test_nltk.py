import nltk

if __name__ == '__main__':
    text = "There are many things you can do to learn how to run faster, such as incorporating speed workouts into your running schedule, running hills, counting your strides, and adjusting your running form. Lean forward when you run and push off firmly with each foot. Pump your arms actively and keep your elbows bent at a 90-degree angle. Try to run every day, and gradually increase the distance you run for long-distance runs. Make sure you rest at least one day per week to allow your body to recover. Avoid running with excess gear that could slow you down."
    nltk.download('punkt')
    sentences = nltk.sent_tokenize(text)
    for sentence in sentences:
        print(sentence)
